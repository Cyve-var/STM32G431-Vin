#include "../Target/Target.h"
#include "../STM32G431/STM32G431_GPIOB_Def.h"
#include "../STM32G431/STM32G431_GPIOA_Def.h"


#define GPIO_INPUT_MODE_0 0
#define GPIO_OUTPUT_MODE_1 1
#define GPIO_ALTERNATE_FUNCTION_MODE_2 2
#define GPIO_ANALOG_MODE_3 3

void GpioInitPortB(void) {
	volatile sSTM32G431_GPIOB* pSTM32G431_GPIOB = (sSTM32G431_GPIOB*)GPIOB_ADR;
	uSTM32G431_GPIOB_MODER STM32G431_GPIOB_MODER;
	uSTM32G431_GPIOB_AFRH STM32G431_GPIOB_AFRH;
	uSTM32G431_GPIOB_AFRL STM32G431_GPIOB_AFRL;
	uSTM32G431_GPIOB_PUPDR STM32G431_GPIOB_PUPDR;

	// Moder:
	STM32G431_GPIOB_MODER.All = pSTM32G431_GPIOB->STM32G431_GPIOB_MODER.All;

	// Matrix OUT 1-8 (PB0-PB7)
	STM32G431_GPIOB_MODER.Bit.MODER0 = GPIO_OUTPUT_MODE_1;
	STM32G431_GPIOB_MODER.Bit.MODER1 = GPIO_OUTPUT_MODE_1;
	STM32G431_GPIOB_MODER.Bit.MODER2 = GPIO_OUTPUT_MODE_1;
	STM32G431_GPIOB_MODER.Bit.MODER3 = GPIO_OUTPUT_MODE_1;
	STM32G431_GPIOB_MODER.Bit.MODER4 = GPIO_OUTPUT_MODE_1;
	STM32G431_GPIOB_MODER.Bit.MODER5 = GPIO_OUTPUT_MODE_1;
	STM32G431_GPIOB_MODER.Bit.MODER6 = GPIO_OUTPUT_MODE_1;
	STM32G431_GPIOB_MODER.Bit.MODER7 = GPIO_OUTPUT_MODE_1;

	// NeoPixel (PB9) - Alternate Function
	STM32G431_GPIOB_MODER.Bit.MODER9 = GPIO_ALTERNATE_FUNCTION_MODE_2;

	// Taster (PB10) - Input
	STM32G431_GPIOB_MODER.Bit.MODER10 = GPIO_INPUT_MODE_0;

	pSTM32G431_GPIOB->STM32G431_GPIOB_MODER.All = STM32G431_GPIOB_MODER.All;

	// Alternate Functions
	STM32G431_GPIOB_AFRH.All = pSTM32G431_GPIOB->STM32G431_GPIOB_AFRH.All;
	STM32G431_GPIOB_AFRH.Bit.AFRH9 = 2; // PB9 auf AF2 (TIM3_CH2)
	pSTM32G431_GPIOB->STM32G431_GPIOB_AFRH.All = STM32G431_GPIOB_AFRH.All;

	// Alternate Functions Low:
	STM32G431_GPIOB_AFRL.All = pSTM32G431_GPIOB->STM32G431_GPIOB_AFRL.All;
	pSTM32G431_GPIOB->STM32G431_GPIOB_AFRL.All = STM32G431_GPIOB_AFRL.All;

	// Pull Up PUll Down Register:
	STM32G431_GPIOB_PUPDR.All = pSTM32G431_GPIOB->STM32G431_GPIOB_PUPDR.All;

	// Taster (PB10) - Pull-up
	STM32G431_GPIOB_PUPDR.Bit.PUPDR10 = 1;

	pSTM32G431_GPIOB->STM32G431_GPIOB_PUPDR.All = STM32G431_GPIOB_PUPDR.All;
}

void GpioInitPortA(void) {
	volatile sSTM32G431_GPIOA* pSTM32G431_GPIOA = (sSTM32G431_GPIOA*)GPIOA_ADR;
	uSTM32G431_GPIOA_MODER STM32G431_GPIOA_MODER;
	uSTM32G431_GPIOA_AFRH STM32G431_GPIOA_AFRH;
	uSTM32G431_GPIOA_AFRL STM32G431_GPIOA_AFRL;
	uSTM32G431_GPIOA_PUPDR STM32G431_GPIOA_PUPDR;

	// MODER
	STM32G431_GPIOA_MODER.All = pSTM32G431_GPIOA->STM32G431_GPIOA_MODER.All;
	
	// Matrix IN 1-4 (PA0-PA3) - Input
	STM32G431_GPIOA_MODER.Bit.MODER0 = GPIO_INPUT_MODE_0;
	STM32G431_GPIOA_MODER.Bit.MODER1 = GPIO_INPUT_MODE_0;
	STM32G431_GPIOA_MODER.Bit.MODER2 = GPIO_INPUT_MODE_0;
	STM32G431_GPIOA_MODER.Bit.MODER3 = GPIO_INPUT_MODE_0;

	// Piezo Mik 1-4 (PA4-PA7) - Analog
	STM32G431_GPIOA_MODER.Bit.MODER4 = GPIO_ANALOG_MODE_3;
	STM32G431_GPIOA_MODER.Bit.MODER5 = GPIO_ANALOG_MODE_3;
	STM32G431_GPIOA_MODER.Bit.MODER6 = GPIO_ANALOG_MODE_3;
	STM32G431_GPIOA_MODER.Bit.MODER7 = GPIO_ANALOG_MODE_3;

	// Matrix IN 5-7 (PA8-PA10) - Input
	STM32G431_GPIOA_MODER.Bit.MODER8 = GPIO_INPUT_MODE_0;
	STM32G431_GPIOA_MODER.Bit.MODER9 = GPIO_INPUT_MODE_0;
	STM32G431_GPIOA_MODER.Bit.MODER10 = GPIO_INPUT_MODE_0;

	// USB D-, D+ (PA11, PA12) - Alternate Function
	STM32G431_GPIOA_MODER.Bit.MODER11 = GPIO_ALTERNATE_FUNCTION_MODE_2;
	STM32G431_GPIOA_MODER.Bit.MODER12 = GPIO_ALTERNATE_FUNCTION_MODE_2;

	// SWDIO, SWCLK (PA13, PA14) - Alternate Function
	STM32G431_GPIOA_MODER.Bit.MODER13 = GPIO_ALTERNATE_FUNCTION_MODE_2;
	STM32G431_GPIOA_MODER.Bit.MODER14 = GPIO_ALTERNATE_FUNCTION_MODE_2;

	// Matrix IN 8 (PA15) - Input
	STM32G431_GPIOA_MODER.Bit.MODER15 = GPIO_INPUT_MODE_0;

	pSTM32G431_GPIOA->STM32G431_GPIOA_MODER.All = STM32G431_GPIOA_MODER.All;

	// Alternate Functions HIGH
	STM32G431_GPIOA_AFRH.All = pSTM32G431_GPIOA->STM32G431_GPIOA_AFRH.All;
	STM32G431_GPIOA_AFRH.Bit.AFRH11 = 10; // PA11 -> AF10 (USB_DM)
	STM32G431_GPIOA_AFRH.Bit.AFRH12 = 10; // PA12 -> AF10 (USB_DP)
	STM32G431_GPIOA_AFRH.Bit.AFRH13 = 0;  // PA13 -> AF0 (SYS_AF)
	STM32G431_GPIOA_AFRH.Bit.AFRH14 = 0;  // PA14 -> AF0 (SYS_AF)
	pSTM32G431_GPIOA->STM32G431_GPIOA_AFRH.All = STM32G431_GPIOA_AFRH.All;

	//Alternate Functions LOW
	STM32G431_GPIOA_AFRL.All = pSTM32G431_GPIOA->STM32G431_GPIOA_AFRL.All;
	pSTM32G431_GPIOA->STM32G431_GPIOA_AFRL.All = STM32G431_GPIOA_AFRL.All;

	// Pull Up PUll Down Register:
	STM32G431_GPIOA_PUPDR.All = pSTM32G431_GPIOA->STM32G431_GPIOA_PUPDR.All;
	
	// Matrix Inputs (PA0-PA3, PA8-PA10, PA15) - Pull-up
	STM32G431_GPIOA_PUPDR.Bit.PUPDR0 = 1;
	STM32G431_GPIOA_PUPDR.Bit.PUPDR1 = 1;
	STM32G431_GPIOA_PUPDR.Bit.PUPDR2 = 1;
	STM32G431_GPIOA_PUPDR.Bit.PUPDR3 = 1;
	STM32G431_GPIOA_PUPDR.Bit.PUPDR8 = 1;
	STM32G431_GPIOA_PUPDR.Bit.PUPDR9 = 1;
	STM32G431_GPIOA_PUPDR.Bit.PUPDR10 = 1;
	STM32G431_GPIOA_PUPDR.Bit.PUPDR15 = 1;

	pSTM32G431_GPIOA->STM32G431_GPIOA_PUPDR.All = STM32G431_GPIOA_PUPDR.All;
}

void GpioInit(void) {
	GpioInitPortB();
	GpioInitPortA();
}
